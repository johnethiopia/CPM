    private void btnAddPatientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddPatientMouseClicked
        // TODO add your handling code here:
        try {
            int result = 0;
            String pname = this.txtPname.getText();
            String page = this.txtPAge.getText();
            String pweghit = this.txtPWeghit.getText();
            String proom = this.txtPRoom.getText();
            String pbodyt = this.txtPBT.getText();
            String pulse = this.txtPulse.getText();
            String prt = this.txtPRT.getText();
            String pbp = this.txtPBP.getText();
            String pdate = this.txtPDate.getText();
            String pdr = this.txtPDr.getText();
            String pnote = this.txtPNote.getText();
            String phone = this.txtPno.getText();
            String pgender = this.cbPGenderr.getText();
            String pblood = this.cbPBloodr.getText();
            String ac = "Active";
            

            
            addpat = con.prepareStatement(
                " insert into patient (Name,Gender,BloodGroup,Age,"
                + "Weghit,RoomNumber,BodyT,PulseRate,ResT,BloodPressure,Date,Status,Doctor,Notes,phone)"
                + " values (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)"
            );

            addpat.setString(1, pname);
            addpat.setString(2, pgender);
            addpat.setString(3, pblood);
            addpat.setString(4, page);
            addpat.setString(5, pweghit);
            addpat.setString(6, proom);
            addpat.setString(7, pbodyt);
            addpat.setString(8, pulse);
            addpat.setString(9, prt);
            addpat.setString(10, pbp);
            addpat.setString(11, pdate);
            addpat.setString(12, ac);
            addpat.setString(13, pdr);
            addpat.setString(14, pnote);
            addpat.setString(15, phone);

            result = addpat.executeUpdate();
            addpat.close();
            pa();
            PaClear();
            sql.close();

        } catch (SQLException s) {
            JOptionPane.showMessageDialog(null, "Make sure that you entered all the fields");
            s.printStackTrace();
        }
    }//GEN-LAST:event_btnAddPatientMouseClicked
