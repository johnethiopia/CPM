 public void pa() {
        try {
            sql = con.createStatement();
            rs = sql.executeQuery(
                    "select CardNumber,Name from patient "
                    + "where Name= " + "'" + txtPname.getText() + "'");
            while (rs.next()) {
                String ID = rs.getString("CardNumber");
                JOptionPane.showMessageDialog(null, "Patient Successfully Added! With Card number " + ID);

            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }
